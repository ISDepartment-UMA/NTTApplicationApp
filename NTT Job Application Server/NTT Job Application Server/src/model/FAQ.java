package model;

public class FAQ {
	private String number;
	private String question;
	private String answer;
	private String average_rates;
	private String videoId;
	private String video_header;
	
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
	public String getQuestion() {
		return question;
	}
	public void setQuestion(String question) {
		this.question = question;
	}
	public String getAnswer() {
		return answer;
	}
	public void setAnswer(String answer) {
		this.answer = answer;
	}
	public String getAverage_rates() {
		return average_rates;
	}
	public void setAverage_rates(String average_rates) {
		this.average_rates = average_rates;
	}
	public String getVideoId() {
		return videoId;
	}
	public void setVideoId(String videoId) {
		this.videoId = videoId;
	}
	public String getVideo_header() {
		return video_header;
	}
	public void setVideo_header(String video_header) {
		this.video_header = video_header;
	}
	 
	

}
