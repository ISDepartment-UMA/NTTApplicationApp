package model;

public class Applications {
	private String device_id;
	private String job_ref_no;
	private String apply_time;
	private String application_status;
	private String email;
	private String first_name;
	private String last_name;
	private String address;
	private String phone_no;
	private String resume_dropbox_url;
	public String getResume_dropbox_url() {
		return resume_dropbox_url;
	}
	public void setResume_dropbox_url(String resume_dropbox_url) {
		this.resume_dropbox_url = resume_dropbox_url;
	}
	public String getDevice_id() {
		return device_id;
	}
	public void setDevice_id(String device_id) {
		this.device_id = device_id;
	}
	public String getJob_ref_no() {
		return job_ref_no;
	}
	public void setJob_ref_no(String job_ref_no) {
		this.job_ref_no = job_ref_no;
	}
	public String getApply_time() {
		return apply_time;
	}
	public void setApply_time(String apply_time) {
		this.apply_time = apply_time;
	}
	public String getApplication_status() {
		return application_status;
	}
	public void setApplication_status(String application_status) {
		this.application_status = application_status;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getFirst_name() {
		return first_name;
	}
	public void setFirst_name(String first_name) {
		this.first_name = first_name;
	}
	public String getLast_name() {
		return last_name;
	}
	public void setLast_name(String last_name) {
		this.last_name = last_name;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getPhone_no() {
		return phone_no;
	}
	public void setPhone_no(String phone_no) {
		this.phone_no = phone_no;
	}
	

}
